public class Box {

	private int width;
	private int height;
	private int length;

	public void inisilazetion() {

		int width = 0;
		int height = 0;
		int length = 0;

	}

	public void setWidth(int width) {
		this.width = width;

	}

	public void setHeight(int height) {
		this.height = height;

	}

	public void setLength(int length) {
		this.length = length;

	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}

	public int getLength() {
		return length;
	}



}
